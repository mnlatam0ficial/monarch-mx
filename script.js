// FUNCIONES DE BÚSQUEDA Y FILTRADO
// ==========================================

function searchProducts(query) {
    return products.filter(product => 
        product.nombre.toLowerCase().includes(query.toLowerCase()) ||
        product.marca.toLowerCase().includes(query.toLowerCase()) ||
        product.categoria.toLowerCase().includes(query.toLowerCase())
    );
}

function getProductsByCategory(category) {
    return products.filter(product => product.categoria === category);
}

function getHighlightedProducts() {
    return products.filter(product => product.destacado === true);
}

function sortByPrice(ascending = true) {
    const sorted = [...products];
    return sorted.sort((a, b) => ascending ? a.precio - b.precio : b.precio - a.precio);
}

function getCategories() {
    return [...new Set(products.map(p => p.categoria))];
}

// ==========================================
// FUNCIONES DE WHATSAPP
// ==========================================

function openWhatsApp(productName = '') {
    const message = productName 
        ? `Hola, me interesa el modelo: ${productName}. ¿Puedes pasarme más info?`
        : WHATSAPP_MESSAGE;
    
    const encodedMessage = encodeURIComponent(message);
    const whatsappUrl = `https://wa.me/${WHATSAPP_NUMBER}?text=${encodedMessage}`;
    window.open(whatsappUrl, '_blank');
}

// ==========================================
// FUNCIONES DE CARRITO (Opcional)
// ==========================================

let cart = [];

function addToCart(productId, quantity = 1) {
    const product = products.find(p => p.id === productId);
    if (!product) return false;
    
    const existingItem = cart.find(item => item.id === productId);
    if (existingItem) {
        existingItem.quantity += quantity;
    } else {
        cart.push({ ...product, quantity });
    }
    return true;
}

function removeFromCart(productId) {
    cart = cart.filter(item => item.id !== productId);
}

function getCartTotal() {
    return cart.reduce((total, item) => total + (item.precio * item.quantity), 0);
}

function getCartItems() {
    return cart;
}

function clearCart() {
    cart = [];
}

function generateCartMessage() {
    if (cart.length === 0) return '';
    
    let message = 'Mi carrito de Monarch Mx:\n\n';
    cart.forEach(item => {
        message += `${item.nombre} - $${item.precio} x ${item.quantity} = $${item.precio * item.quantity}\n`;
    });
    message += `\nTotal: $${getCartTotal()}\n\nQuiero asegurar mi pedido.`;
    
    return message;
}

function sendCartToWhatsApp() {
    const message = generateCartMessage();
    if (!message) {
        alert('Tu carrito está vacío');
        return;
    }
    
    const encodedMessage = encodeURIComponent(message);
    const whatsappUrl = `https://wa.me/${WHATSAPP_NUMBER}?text=${encodedMessage}`;
    window.open(whatsappUrl, '_blank');
}

